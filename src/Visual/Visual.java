        String conferencia = ""+tabla.getValueAt(clic, 3);
        String Taller = ""+tabla.getValueAt(clic, 4);
        String Fecha = ""+tabla.getValueAt(clic, 5);
        String Hora = ""+tabla.getValueAt(clic, 6);
        String FechaT = ""+tabla.getValueAt(clic, 7);
        String HoraT = ""+tabla.getValueAt(clic, 8);
