    public void agregar(File ruta, String nombre, String conferencia, String Taller, String Fecha, String Hora,String FechaT, String HoraT, float costo,float costoT){
        ProductoVO vo = new ProductoVO();
        dao = new ProductoDAO();
        
      //  vo.setClave(clave);
        vo.setNombreConferencista(nombre);
        vo.setNombreConferencia(conferencia);
        vo.setNombreTaller(Taller);
        vo.setFechaa(Fecha);
        vo.setHoraa(Hora);
        vo.setFechaT(FechaT);
        vo.setHoraT(HoraT);
        vo.setCosto(costo);
        vo.setCostoT(costoT);
        try{
            byte[] icono = new byte[(int) ruta.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(icono);
            vo.setFoto(icono);
        }catch(Exception ex){
            vo.setFoto(null);
        }
        dao.Agregar_ProductoVO(vo);
        limpiar();
    }
    
